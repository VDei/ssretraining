# Ask user to enter two numbers
number_a = float(input('Input number A? - '))
number_b = float(input('Input number B? - '))

# Show results of math functions
print(f'A + B = {number_a + number_b}')
print(f'A - B = {number_a - number_b}')
print(f'A * B = {number_a * number_b}')
print(f'A / B = {number_a / number_b}')
print(f'A ** B = {number_a ** number_b}')
print(f'A // B = {number_a // number_b}')
print(f'A % B = {number_a % number_b}')